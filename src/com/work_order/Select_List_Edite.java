    private void addData() {
        try {
            ResultSet search = DB.search("SELECT * FROM order_list WHERE status=1");
            DefaultTableModel dtm = (DefaultTableModel) tb1.getModel();
            while(search.next()){
            Vector v = new Vector();
            v.add(search.getString("id"));
                ResultSet search1 = DB.search("SELECT * FROM customer WHERE customer_id='"+search.getString("customer_id")+"'");
                if(search1.next()){
                v.add(search1.getString("customer_id")+"-"+search1.getString("first_name")+" "+search1.getString("last_name"));
                }
                ResultSet search2 = DB.search("SELECT * FROM vehicle WHERE vehicle_id='"+search.getString("vehicle_number")+"'");
                if(search2.next()){
                v.add(search2.getString("vehicle_id")+":"+search2.getString("province")+" "+search2.getString("registration_number"));
                }
                v.add(search.getString("location"));
                v.add(search.getString("date"));
                v.add(search.getString("discription"));
            dtm.addRow(v);
            }
            
            
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void editeJob() {
        try {
              new Edite_List_Order().setVisible(true);
           this.dispose();
            ResultSet search = DB.search("SELECT * FROM order_list WHERE id='"+id+"'");
            if(search.next()){
         
           Edite_List_Order.txtOrderId.setText(search.getString("id"));
           Edite_List_Order.txtCustomerId.setText(search.getString("customer_id"));
           Edite_List_Order.txtVehicleNumber.setText(search.getString("vehicle_number"));
           Edite_List_Order.txtLocation.setText(search.getString("location"));
           Edite_List_Order.txtDiscription.setText(search.getString("discription"));  
           Edite_List_Order.txtDate.setDate(search.getDate("date"));  
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

