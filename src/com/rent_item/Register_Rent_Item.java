 private void generateOrId() {
        try {
            txtItemId.setEditable(false);
            ResultSet rs = DB.search("select count(item_id) as x from rent_item");
            if (rs.next()) {
                String counts = rs.getString("x");
                int count = Integer.parseInt(counts);
                ++count;
                if (count < 10) {
                    txtItemId.setText("IT00000" + count);
                } else if (count < 100) {
                    txtItemId.setText("IT0000" + count);
                } else if (count < 1000) {
                    txtItemId.setText("IT000" + count);
                } else if (count < 10000) {
                    txtItemId.setText("IT00" + count);
                } else if (count < 100000) {
                    txtItemId.setText("IT0" + count);
                } else if (count < 1000000) {
                    txtItemId.setText("IT" + count);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    private void registerItem() {
        try {
            if(!txtItemName.getText().equals("")){
                if(!txtModel.getText().equals("")){
                    String productNum=null;
                    if(txtProductNumber.equals("")){
                    productNum="null";
                    }else{
                    productNum=txtProductNumber.getText().toUpperCase();
                    }
            String dataTime = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());
            DB.iud("INSERT INTO rent_item VALUES('" + txtItemId.getText().toUpperCase() + "','" + txtItemName.getText().toUpperCase() + "','" + txtModel.getText().toUpperCase() + "','" + txtCompany.getText().toUpperCase() + "','" + productNum + "',"
                    + "'" + txtDiscription.getText().toUpperCase() + "','" + dataTime + "','" + 1 + "')");
                          JOptionPane.showMessageDialog(this,"Seccessful");
                          clearFeald();
            }else{
                 JOptionPane.showMessageDialog(this, "Please Enter Item Model", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
             JOptionPane.showMessageDialog(this, "Please Enter Item Name", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

