    private void searchOrder(String orderId) {
        try {
            ResultSet search = DB.search("SELECT * FROM work_order WHERE order_id='"+orderId+"'");
            if(search.next()){
            txtOrderId.setText(search.getString("order_id"));
            ResultSet search1 = DB.search("SELECT * FROM customer WHERE customer_id='"+search.getString("customer_id")+"'");
            if(search1.next()){
            txtCustomerId.setText(search.getString("customer_id")+"-"+search1.getString("first_name")+" "+search1.getString("last_name"));
            }
            ResultSet search2 = DB.search("SELECT * FROM vehicle WHERE vehicle_id='"+search.getString("vehicle_id")+"'");
            if(search2.next()){
            txtVehicleNumber.setText(search.getString("vehicle_id")+"_"+search2.getString("registration_number"));
            }
            txtLastMeter.setText(search.getString("start_meter"));
            txtLocation.setText(search.getString("location"));
            txtDiscription.setText("discription");
            
                ResultSet search3 = DB.search("SELECT * FROM employee_job WHERE order_id='"+txtOrderId.getText()+"'");
                DefaultTableModel dtm =(DefaultTableModel) tb3.getModel();
                dtm.setRowCount(0);
              while(search3.next()){
                   Vector v = new Vector();
                   v.add(search3.getString("employee_id"));
                ResultSet search4 = DB.search("SELECT * FROM employee WHERE employee_id='"+search3.getString("employee_id")+"'");
                if(search4.next()){
                v.add(search4.getString("nic_number"));
                v.add(search4.getString("first_name")+" "+search4.getString("last_name"));
                v.add(search4.getString("phone_number"));
                dtm.addRow(v);
                }

            }
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

