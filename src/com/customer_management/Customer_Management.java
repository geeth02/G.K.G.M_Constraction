 private void tbData() {
         try {
            DefaultTableModel dtm =(DefaultTableModel) tb1.getModel();
           ResultSet search = DB.search("SELECT * FROM customer WHERE status=1");
           dtm.setRowCount(0);
           while(search.next()){
               Vector v = new Vector();
               v.add(search.getString("customer_id"));
               v.add(search.getString("first_name")+" "+search.getString("last_name"));
               v.add(search.getString("nic_number"));
               v.add(search.getString("phone_number"));
                  v.add(search.getString("gender"));
                  v.add(search.getString("address"));
                    v.add(search.getString("data_time"));
                ResultSet search1 = DB.search("SELECT * FROM employee WHERE employee_id='"+search.getString("employee_id")+"'");
               if(search1.next()){
               v.add(search1.getString("employee_id")+"-"+search1.getString("first_name")+" "+search1.getString("last_name"));
               }
               
               dtm.addRow(v);
               }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

